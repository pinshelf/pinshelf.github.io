// Imports /////////////////////////////////////////////////////////////////////
import { z } from 'zod'
import { PageSchema } from './homescreen'

// Constants ///////////////////////////////////////////////////////////////////
export const PINSHELF_SETTINGS_TITLE = 'Pinshelf Settings'

// Schema //////////////////////////////////////////////////////////////////////
export const SettingsSchema = z.object({
    homescreen: z.object({
        pages: PageSchema.array(),
        currentPageIndex: z.number(),
    })
})

export type Settings = z.infer<typeof SettingsSchema>

